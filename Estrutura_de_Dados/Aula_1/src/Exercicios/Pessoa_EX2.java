package Exercicios;

public class Pessoa_EX2 {
    
    private int idade;
    private double peso;
    private double altura;

    public Pessoa_EX2(int idade, double peso, double altura) {
        this.idade = idade;
        this.peso = peso;
        this.altura = altura;
    }

    public int getIdade() {
        return idade;
    }

    public void setIdade(int idade) {
        this.idade = idade;
    }

    public double getPeso() {
        return peso;
    }

    public void setPeso(double peso) {
        this.peso = peso;
    }

    public double getAltura() {
        return altura;
    }

    public void setAltura(double altura) {
        this.altura = altura;
    }
    
    
}
